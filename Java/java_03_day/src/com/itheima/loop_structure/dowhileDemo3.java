package com.itheima.loop_structure;

public class dowhileDemo3 {
    public static void main(String[] args) {
        int i = 1;
        do {
            System.out.println("hello world");
            i++;
        } while (i <= 5);
        System.out.println("循环结束");
        System.out.println("i = " + i);
        System.out.println("程序结束");
    }
}
